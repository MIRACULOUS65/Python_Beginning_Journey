"""
Explanation of: for k in range(2, 20, 3):
"""

print("The range(2, 20, 3) function generates numbers:")
print("- Start: 2 (beginning value)")
print("- Stop: 20 (ending value, not inclusive)")
print("- Step: 3 (increment between each value)")
print()

print("So the loop will print:")
for k in range(2, 20, 3):
    print(k)

print()
print("Sequence: 2, 5, 8, 11, 14, 17")
print("The loop stops before reaching 20.")
